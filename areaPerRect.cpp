// This program calculates the area and perimeter of a rectangle.

#include <iostream>

int main() {
    std::cout << "If a rectangle has a length of";
    std::cout << " 7 cm and width of 4cm \n";
    std::cout << "\n";
    std::cout << "Area of the rectangle is " << 7*4 << "cm^2.\n";
    std::cout << "Perimeter of the rectangle is " << 2*(7+4) << "cm.\n";
}
